package com.ccwl.manager.model;

public class SchoolCard {
    String number;
    int hotWaterBalance;
    int mealCardBalance;

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public int getHotWaterBalance() {
        return hotWaterBalance;
    }

    public void setHotWaterBalance(int hotWaterBalance) {
        this.hotWaterBalance = hotWaterBalance;
    }

    public int getMealCardBalance() {
        return mealCardBalance;
    }

    public void setMealCardBalance(int mealCardBalance) {
        this.mealCardBalance = mealCardBalance;
    }
}
